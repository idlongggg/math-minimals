/**
 * Tạo breadcrumb cho navigation
 * @param currentPath - Đường dẫn hiện tại
 * @returns Mảng các breadcrumb items
 */
export const createBreadcrumb = (currentPath: string) => {
  const pathSegments = currentPath.split('/').filter(Boolean);
  const breadcrumbs = [];

  let cumulativePath = '';
  for (const segment of pathSegments) {
    cumulativePath += `/${segment}`;
    breadcrumbs.push({
      name: segment.replace(/-/g, ' ').replace(/\b\w/g, (l) => l.toUpperCase()),
      path: cumulativePath,
    });
  }

  return breadcrumbs;
};

/**
 * Kiểm tra xem một path có phải là con của path khác không
 * @param childPath - Path con
 * @param parentPath - Path cha
 * @returns Boolean
 */
export const isChildPath = (childPath: string, parentPath: string): boolean => {
  return childPath.startsWith(parentPath) && childPath !== parentPath;
};

/**
 * Lấy tất cả các path con của một path
 * @param parentPath - Path cha
 * @returns Mảng các path con
 */
export const getChildPaths = (parentPath: string): string[] => {
  const allPaths = Object.values(paths.dashboard).flat();
  return allPaths.filter((path) => typeof path === 'string' && isChildPath(path, parentPath));
};

/**
 * Tìm path parent gần nhất
 * @param currentPath - Path hiện tại
 * @returns Parent path hoặc null
 */
export const findParentPath = (currentPath: string): string | null => {
  const pathSegments = currentPath.split('/').filter(Boolean);
  if (pathSegments.length <= 2) return null; // dashboard level

  pathSegments.pop(); // Remove last segment
  return '/' + pathSegments.join('/');
};

